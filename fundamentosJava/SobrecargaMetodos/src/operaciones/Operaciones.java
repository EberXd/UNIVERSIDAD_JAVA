public class Operaciones {
    
    ///la sobre carga de metodoso consiste en tener
    ///varios metodos con el mismo nombre
    ///pero que reciben una cantidad diferente de argumentos
    //o diferentes tipos de argumentos
    
    
    public static int sumar(int a, int b){
       
        return a+b;
        
    }
    
    ///en este metodo la diferencia es el tipo de los argumentos
    public static double sumar(double a, double b){
        
        
        return a+b;
        
    }
    
    
    
    
}
